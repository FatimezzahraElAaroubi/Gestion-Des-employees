    private void btntriActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btntriActionPerformed
       
        operations metier1 = new operations();
        model.setRowCount(0);
         String code=comborech.getSelectedItem().toString();
        try {
            if (des.isSelected()){
            ArrayList<Employe> liste = metier1.trieDes(code);
           Object[] row=new Object[7];
           for(int i =0;i<liste.size();i++){
               row[0]=liste.get(i).getCodeEmp();
                 row[1]=liste.get(i).getNom();
                   row[2]=liste.get(i).getPrenom();
                   row[3]=liste.get(i).getAdresse();
                   row[4]=liste.get(i).getTelephone();
                   row[5]=liste.get(i).getSex();
                   row[6]=liste.get(i).getService();
                     model.addRow(row);     
       }
               table.setModel(model);}
            else if (Asd.isSelected()){
                   operations metier = new operations();
                ArrayList<Employe> list = metier.trieAsd(code);
           Object[] row=new Object[7];
           for(int i =0;i<list.size();i++){
               row[0]=list.get(i).getCodeEmp();
                 row[1]=list.get(i).getNom();
                   row[2]=list.get(i).getPrenom();
                   row[3]=list.get(i).getAdresse();
                   row[4]=list.get(i).getTelephone();
                   row[5]=list.get(i).getSex();
                   row[6]=list.get(i).getService();
                     model.addRow(row);     
       }
               table.setModel(model);}
       }catch (Exception e) { System.err.println(e);
       JOptionPane.showMessageDialog(null,e.getMessage());
       }
    }//GEN-LAST:event_btntriActionPerformed

